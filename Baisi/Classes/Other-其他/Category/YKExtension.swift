import UIKit


//UIView的宽度高度快捷操作
extension UIView{
    
    //返回宽度
    func yk_width()-> CGFloat{
        return self.frame.size.width
    }
    
    //返回高度
    func yk_height()-> CGFloat{
        return self.frame.size.height
    }
    


}
